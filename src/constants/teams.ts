/** Flatten all groups for a sport into a single item list (for search-term lookups). */
export function allItemsForSport(sportId: SportId) {
  return SPORT_TEAMS[sportId]?.groups.flatMap((g) => g.items) ?? [];
}

// Group labels whose items are individual people rather than teams/clubs —
// the schedule lookup resolves these via player search (player → their team).
const PLAYER_GROUP_LABELS = new Set(["Men's ATP", "Women's WTA", 'Players', 'Drivers', 'Fighters']);

export interface FollowedEntity {
  kind: 'team' | 'player';
  sport: SportId;
  name: string;
}

/** All selected picks across sports, tagged team/player for schedule lookups. */
export function followedEntities(teams: Partial<Record<SportId, string[]>>): FollowedEntity[] {
  const out: FollowedEntity[] = [];
  for (const [sportId, ids] of Object.entries(teams)) {
    if (!ids?.length) continue;
    for (const group of SPORT_TEAMS[sportId as SportId]?.groups ?? []) {
      const kind = PLAYER_GROUP_LABELS.has(group.label) ? 'player' : 'team';
      for (const item of group.items) {
        if (ids.includes(item.id)) out.push({ kind, sport: sportId as SportId, name: item.name });
      }
    }
  }
  return out;
}
